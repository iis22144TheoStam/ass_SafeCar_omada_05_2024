public class Nea_Diadikasia2 {
    private String titlos;
    private String perigrafh;
    private String stoxoi;
    private String stadia;

    public Nea_Diadikasia2(String titlos, String perigrafh, String stoxoi, String stadia) {
        this.titlos = titlos;
        this.perigrafh = perigrafh;
        this.stoxoi = stoxoi;
        this.stadia = stadia;
    }

    public String getTitlos() {
        return titlos;
    }

    public void setTitlos(String titlos) {
        this.titlos = titlos;
    }

    public String getPerigrafh() {
        return perigrafh;
    }

    public void setPerigrafh(String perigrafh) {
        this.perigrafh = perigrafh;
    }

    public String getStoxoi() {
        return stoxoi;
    }

    public void setStoxoi(String stoxoi) {
        this.stoxoi = stoxoi;
    }

    public String getStadia() {
        return stadia;
    }

    public void setStadia(String stadia) {
        this.stadia = stadia;
    }
}

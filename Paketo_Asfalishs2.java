public class Paketo_Asfalishs2 {
    private String name;
    private String kalypseis;
    private String times;

    public Paketo_Asfalishs2(String name, String kalypseis, String times) {
        this.name = name;
        this.kalypseis = kalypseis;
        this.times = times;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKalypseis() {
        return kalypseis;
    }

    public void setKalypseis(String kalypseis) {
        this.kalypseis = kalypseis;
    }

    public String getTimes() {
        return times;
    }

    public void setTimes(String times) {
        this.times = times;
    }
}
